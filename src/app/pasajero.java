    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String Identificacion = txtID.getText();
        String Nombre = txtNombre.getText();
        int Edad = Integer.parseInt(txtEdad.getText());
        String Email = txtEmail.getText();
        String Cod_Reserva = txtReserva.getText();
        String Destino = txtDestino.getText();
        
        
        try {
            Connection con = conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO Pasajeros (Identificacion, Nombre, Edad, Email, Cod_Reserva, Destino, activo) VALUES (?,?,?,?,?,?,?) ");
            ps.setString(1, Identificacion);
            ps.setString(2, Nombre);
            ps.setInt(3, Edad);
            ps.setString(4, Email);
            ps.setString(5, Cod_Reserva);
            ps.setString(6, Destino);
            ps.setInt(7, 1);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Guardado");
            Limpiar();
            cargarTablaPasajeros();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

