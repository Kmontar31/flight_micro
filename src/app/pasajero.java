    private void tblPasajerosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPasajerosMouseClicked
       try {
            int fila = tblPasajeros.getSelectedRow();
            int codigo = Integer.parseInt(tblPasajeros.getValueAt(fila, 0).toString());
            PreparedStatement ps; 
            ResultSet rs; 
            
            Connection con = conexion.getConexion();
            ps = con.prepareStatement("SELECT Identificacion, Nombre, Edad, Email, Cod_Reserva, Destino FROM Pasajeros WHERE codigo=?");
            ps.setInt(1, codigo);
            rs = ps.executeQuery();
            
            while (rs.next()){
                
                txtID.setText(rs.getString("Identificacion"));
                txtNombre.setText(rs.getString("Nombre"));
                txtEdad.setText(rs.getString("Edad"));
                txtEmail.setText(rs.getString("Email"));
                txtReserva.setText(rs.getString("Cod_Reserva"));
                txtDestino.setText(rs.getString("Destino"));
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblPasajerosMouseClicked

